package wire

import (
	"fmt"
	"sort"
	"strings"
)

// DetectCycle returns the cycle path if one exists in the provider graph, nil otherwise.
func DetectCycle(providers map[string]*Provider) []string {
	adj := make(map[string][]string, len(providers))
	for k, p := range providers {
		adj[k] = p.Deps
	}
	visited := make(map[string]int) // 0=unvisited 1=in-stack 2=done
	var path []string

	var dfs func(n string) bool
	dfs = func(n string) bool {
		if visited[n] == 2 {
			return false
		}
		if visited[n] == 1 {
			return true
		}
		visited[n] = 1
		path = append(path, n)
		for _, dep := range adj[n] {
			if dfs(dep) {
				return true
			}
		}
		path = path[:len(path)-1]
		visited[n] = 2
		return false
	}

	for k := range providers {
		path = nil
		if dfs(k) {
			return append(path, path[0])
		}
	}
	return nil
}

// TopoSort returns provider keys in topological (dependency-first) order.
func TopoSort(providers map[string]*Provider) []string {
	visited := make(map[string]bool)
	var order []string

	var visit func(k string)
	visit = func(k string) {
		if visited[k] {
			return
		}
		visited[k] = true
		for _, dep := range providers[k].Deps {
			if _, ok := providers[dep]; ok {
				visit(dep)
			}
		}
		order = append(order, k)
	}

	keys := make([]string, 0, len(providers))
	for k := range providers {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	for _, k := range keys {
		visit(k)
	}
	return order
}

// SubPkg describes one subpackage to be called from a root aggregator.
type SubPkg struct {
	Alias      string // local import alias (equals the Go package name)
	ImportPath string // fully-qualified Go import path
	FuncName   string // exported registration function in that package
}

// BuildDIGen produces the di_gen.go source string.
//
// exportFunc controls the generated function signature:
//   - "" (empty)         → func initDI(app *astra.App) *di.Container  (full mode)
//   - any non-empty name → func <name>(c *di.Container)               (register mode for subpackages)
func BuildDIGen(pkgName string, ordered []string, providers map[string]*Provider, setupFuncs []string, exportFunc string) string {
	var b strings.Builder

	fmt.Fprintf(&b, "// Code generated by astractl gen wire --scan. DO NOT EDIT.\n")
	fmt.Fprintf(&b, "// Regenerate: astractl gen wire --scan --dir . --force\n\n")
	fmt.Fprintf(&b, "package %s\n\n", pkgName)

	registerMode := exportFunc != ""

	// The generated function body only calls setup funcs (same package) and uses
	// di / astra — no user package imports are needed.
	imports := map[string]string{
		"di": "github.com/astra-go/astra/di",
	}
	if !registerMode {
		imports["astra"] = "github.com/astra-go/astra"
	}

	aliases := make([]string, 0, len(imports))
	for alias := range imports {
		aliases = append(aliases, alias)
	}
	sort.Strings(aliases)
	fmt.Fprintf(&b, "import (\n")
	for _, alias := range aliases {
		path := imports[alias]
		lastSeg := path[strings.LastIndex(path, "/")+1:]
		if alias == lastSeg {
			fmt.Fprintf(&b, "\t%q\n", path)
		} else {
			fmt.Fprintf(&b, "\t%s %q\n", alias, path)
		}
	}
	fmt.Fprintf(&b, ")\n\n")

	// Doc comment + dependency graph.
	if registerMode {
		fmt.Fprintf(&b, "// %s registers all %s providers into c.\n", exportFunc, pkgName)
		fmt.Fprintf(&b, "// Call from a root aggregator's initDI before app.Run().\n")
	} else {
		fmt.Fprintf(&b, "// initDI registers all providers with a new di.Container and binds its\n")
		fmt.Fprintf(&b, "// lifecycle to app. Call once in main() before app.Run().\n")
	}
	fmt.Fprintf(&b, "//\n")
	fmt.Fprintf(&b, "// Dependency graph (topological order):\n")
	if len(ordered) == 0 {
		fmt.Fprintf(&b, "//   (no providers found — add di.Provide* calls and re-run gen wire --scan)\n")
	}
	for i, k := range ordered {
		p := providers[k]
		prefix := "├─"
		if i == len(ordered)-1 {
			prefix = "└─"
		}
		label := k
		if p.FuncName != "" {
			label = fmt.Sprintf("%s  ← %s()", k, p.FuncName)
		}
		if p.Named != "" {
			label = fmt.Sprintf("%s [name=%q]", label, p.Named)
		}
		fmt.Fprintf(&b, "//   %s %s\n", prefix, label)
		for j, dep := range p.Deps {
			depPrefix := "│    ├╴"
			if j == len(p.Deps)-1 {
				depPrefix = "│    └╴"
			}
			fmt.Fprintf(&b, "//   %s %s\n", depPrefix, dep)
		}
	}

	// Function signature.
	if registerMode {
		fmt.Fprintf(&b, "func %s(c *di.Container) {\n", exportFunc)
	} else {
		fmt.Fprintf(&b, "func initDI(app *astra.App) *di.Container {\n")
		fmt.Fprintf(&b, "\tc := di.New()\n\n")
	}

	if len(setupFuncs) == 0 {
		fmt.Fprintf(&b, "\t// TODO: add di.Provide* calls in your source files, then re-run:\n")
		fmt.Fprintf(&b, "\t// astractl gen wire --scan --dir . --force\n")
	} else {
		for _, fn := range setupFuncs {
			fmt.Fprintf(&b, "\t%s(c)\n", fn)
		}
	}

	if !registerMode {
		fmt.Fprintf(&b, "\n\tc.BindApp(app)\n")
		fmt.Fprintf(&b, "\treturn c\n")
	}
	fmt.Fprintf(&b, "}\n")

	return b.String()
}

// BuildRootDIGen produces a root di_gen.go that imports every subpackage and
// delegates provider registration to each one's exported registration function.
func BuildRootDIGen(pkgName string, subPkgs []SubPkg) string {
	var b strings.Builder

	fmt.Fprintf(&b, "// Code generated by astractl gen wire --scan --aggregate. DO NOT EDIT.\n")
	fmt.Fprintf(&b, "// Regenerate: astractl gen wire --scan --aggregate --dir . --force\n\n")
	fmt.Fprintf(&b, "package %s\n\n", pkgName)

	type imp struct{ alias, path string }
	imps := []imp{
		{"astra", "github.com/astra-go/astra"},
		{"di", "github.com/astra-go/astra/di"},
	}
	for _, sp := range subPkgs {
		imps = append(imps, imp{sp.Alias, sp.ImportPath})
	}
	sort.Slice(imps, func(i, j int) bool { return imps[i].alias < imps[j].alias })

	fmt.Fprintf(&b, "import (\n")
	for _, im := range imps {
		lastSeg := im.path[strings.LastIndex(im.path, "/")+1:]
		if im.alias == lastSeg {
			fmt.Fprintf(&b, "\t%q\n", im.path)
		} else {
			fmt.Fprintf(&b, "\t%s %q\n", im.alias, im.path)
		}
	}
	fmt.Fprintf(&b, ")\n\n")

	fmt.Fprintf(&b, "// initDI creates a di.Container, delegates registration to each subpackage,\n")
	fmt.Fprintf(&b, "// and binds its lifecycle to app. Call once in main() before app.Run().\n")
	fmt.Fprintf(&b, "func initDI(app *astra.App) *di.Container {\n")
	fmt.Fprintf(&b, "\tc := di.New()\n\n")
	for _, sp := range subPkgs {
		fmt.Fprintf(&b, "\t%s.%s(c)\n", sp.Alias, sp.FuncName)
	}
	fmt.Fprintf(&b, "\n\tc.BindApp(app)\n")
	fmt.Fprintf(&b, "\treturn c\n")
	fmt.Fprintf(&b, "}\n")

	return b.String()
}
