package router

// fastMatcher is a zero-allocation string predicate for common patterns.
type fastMatcher func(s string) bool

// wellKnownMatchers maps common regexp patterns to their fast-path equivalents.
// Checked at route-registration time; any pattern listed here bypasses the
// regexp engine on every request.
var wellKnownMatchers = map[string]fastMatcher{
	`[0-9]+`:          fastDigits,
	`\d+`:             fastDigits,
	`[a-z]+`:          fastLower,
	`[A-Z]+`:          fastUpper,
	`[a-zA-Z]+`:       fastAlpha,
	`[a-z0-9]+`:       fastAlphanumLower,
	`[A-Z0-9]+`:       fastAlphanumUpper,
	`[a-zA-Z0-9]+`:    fastAlphanum,
	`[a-z0-9\-]+`:     fastSlugLower,
	`[a-z0-9-]+`:      fastSlugLower,
	`[a-zA-Z0-9_\-]+`: fastSlug,
	`[a-zA-Z0-9_-]+`:  fastSlug,
	`[a-zA-Z0-9_]+`:   fastIdentifier,
	`\w+`:             fastIdentifier,
	`[0-9a-f]+`:       fastHexLower,
	`[0-9a-fA-F]+`:    fastHex,
}

func fastDigits(s string) bool {
	if len(s) == 0 {
		return false
	}
	for i := 0; i < len(s); i++ {
		if s[i] < '0' || s[i] > '9' {
			return false
		}
	}
	return true
}

func fastLower(s string) bool {
	if len(s) == 0 {
		return false
	}
	for i := 0; i < len(s); i++ {
		if s[i] < 'a' || s[i] > 'z' {
			return false
		}
	}
	return true
}

func fastUpper(s string) bool {
	if len(s) == 0 {
		return false
	}
	for i := 0; i < len(s); i++ {
		if s[i] < 'A' || s[i] > 'Z' {
			return false
		}
	}
	return true
}

func fastAlpha(s string) bool {
	if len(s) == 0 {
		return false
	}
	for i := 0; i < len(s); i++ {
		c := s[i]
		if !((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')) {
			return false
		}
	}
	return true
}

func fastAlphanumLower(s string) bool {
	if len(s) == 0 {
		return false
	}
	for i := 0; i < len(s); i++ {
		c := s[i]
		if !((c >= 'a' && c <= 'z') || (c >= '0' && c <= '9')) {
			return false
		}
	}
	return true
}

func fastAlphanumUpper(s string) bool {
	if len(s) == 0 {
		return false
	}
	for i := 0; i < len(s); i++ {
		c := s[i]
		if !((c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9')) {
			return false
		}
	}
	return true
}

func fastAlphanum(s string) bool {
	if len(s) == 0 {
		return false
	}
	for i := 0; i < len(s); i++ {
		c := s[i]
		if !((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9')) {
			return false
		}
	}
	return true
}

func fastSlugLower(s string) bool {
	if len(s) == 0 {
		return false
	}
	for i := 0; i < len(s); i++ {
		c := s[i]
		if !((c >= 'a' && c <= 'z') || (c >= '0' && c <= '9') || c == '-') {
			return false
		}
	}
	return true
}

func fastSlug(s string) bool {
	if len(s) == 0 {
		return false
	}
	for i := 0; i < len(s); i++ {
		c := s[i]
		if !((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9') || c == '_' || c == '-') {
			return false
		}
	}
	return true
}

func fastIdentifier(s string) bool {
	if len(s) == 0 {
		return false
	}
	for i := 0; i < len(s); i++ {
		c := s[i]
		if !((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9') || c == '_') {
			return false
		}
	}
	return true
}

